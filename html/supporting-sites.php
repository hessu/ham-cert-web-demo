      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#collapseBen">
	Benefits of Certificate Authentication
      </a>
    </div>
    <div id="collapseBen" class="accordion-body collapse">
      <div class="accordion-inner">
<p>Many amateur radio related web sites and Internet services
need to make sure that you, an Internet user, are a licensed
amateur radio operator. This is specifically necessary when a
service lets you initiate radio frequency transmissions on
amateur bands. It would be a huge task for each and
every site to separately validate the license documents
of each site visitor. It'd also be a lot of work for you to
provide the documentation to each site, over and over again.</p>

<p>Instead, you may provide the documentation <b>once</b> to a <b>single</b>
organisation acting as a <b>Certificate Authority</b>, which will
inspect the documents and then issue you a <b>digital certificate</b>.
The certificate is installed in your web browser (or an
amateur radio specific application), which will then
automatically provide it to the services. The browser
may ask you to choose which certificate to give to a site, or ask
for permission to use a certificate at the site.
</p>
      </div>
